puts "hello, world"


# ここで実行されているものは、putsという命令1つだけです。putsは、「受け取った値を文字列として出力」する働きです。
# このように何らかの処理をさせる命令を、Rubyでは「メソッド（関数）」と呼びます。
# また、その後ろに続く記述（ここでは'Hello, World!'）を「引数（ひきすう）」と呼びます。引数では、メソッドに渡す値を記述します。
# putsメソッドの引数'Hello, World!'によって、「Hello, World!」という文字列が出力されます。
# 文字列は、ダブルクォーテーション（"）またはシングルクォーテーション（'）で囲む必要があります。
# この文字列をputsメソッドが受け取り、「Hello, World!」と出力して表示しています。